    private void comboListaHabitacionesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboListaHabitacionesActionPerformed
        if ((String) (comboListaHabitaciones.getSelectedItem()) != "---") {
            try {
                Properties propiedad = new Properties();
                propiedad.load(new FileInputStream("C:\\Users\\Gestec\\Desktop\\Hotel\\" + (String) (comboListaHabitaciones.getSelectedItem()) + ".properties"));

                tNumeroHabitacion.setText(propiedad.getProperty("NumeroDeHabitacion"));
                tCapacidad.setText(propiedad.getProperty("Capacidad"));
                tValorPorDia.setText(propiedad.getProperty("CostoPorDia"));
                tDescripcion.setText(propiedad.getProperty("Descripcion"));
                tDisponibilidad.setText(propiedad.getProperty("Disponibilidad"));
                
                ImageIcon foto = new ImageIcon(propiedad.getProperty("Fotografia"));
                Image ajuste = foto.getImage().getScaledInstance(labelFoto.getWidth(), labelFoto.getHeight(), Image.SCALE_SMOOTH);
                Icon imagenAjustada = new ImageIcon(ajuste);
        
                labelFoto.setIcon(imagenAjustada);
            } catch (IOException ex) {
                    JOptionPane.showMessageDialog(null, "La habitación que está consultando no existe o fue revocada recientemente.");
            }
        } else {
            tNumeroHabitacion.setText("");
            tCapacidad.setText("");
            tDescripcion.setText("");
            tValorPorDia.setText("");
            tDisponibilidad.setText("");
        }
    }//GEN-LAST:event_comboListaHabitacionesActionPerformed

