    public void manejoArchivo(){
        String numeroConsulta = tConsulta.getText();
        if(!numeroConsulta.equals("")){
        try {
                Properties propiedad = new Properties();
                propiedad.load(new FileInputStream("C:\\Users\\Gestec\\Desktop\\Hotel\\Hab_"+numeroConsulta+".properties"));

                tNumeroHabitacion.setText(propiedad.getProperty("NumeroDeHabitacion"));
                tCapacidad.setText(propiedad.getProperty("Capacidad"));
                tValorPorDia.setText(propiedad.getProperty("CostoPorDia"));
                tDescripcion.setText(propiedad.getProperty("Descripcion"));
                tDisponibilidad.setText(propiedad.getProperty("Disponibilidad"));
                
                ImageIcon foto = new ImageIcon(propiedad.getProperty("Fotografia"));
                Image ajuste = foto.getImage().getScaledInstance(labelFoto.getWidth(), labelFoto.getHeight(), Image.SCALE_SMOOTH);
                Icon imagenAjustada = new ImageIcon(ajuste);
        
                labelFoto.setIcon(imagenAjustada);
            }
    catch (IOException ex) {
                    JOptionPane.showMessageDialog(null, "No hay habitación con ese número.");
                }
            }
    }
    
    public void leerHabitacionesCreadas() {
        try {
            File archivo = new File("C:\\Users\\Gestec\\Desktop\\Hotel\\HabitacionesCreadas.txt");
            Scanner leer = new Scanner(archivo);
            Properties propiedad = new Properties();
            int i = 0;
            while (leer.hasNextLine()) {
                lineasLeer.add(leer.nextLine());
                propiedad.load(new FileInputStream("C:\\Users\\Gestec\\Desktop\\Hotel\\"+lineasLeer.get(i)+".properties"));
                if(propiedad.getProperty("Disponibilidad").equals("Si")){
                    comboListaHabitaciones.addItem(lineasLeer.get(i));
                }
                i++;
            }
        } catch (IOException exception) {
            JOptionPane.showMessageDialog(null, exception);
        }
    }
