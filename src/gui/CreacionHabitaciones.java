    private void bCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCrearActionPerformed
   
        if(tNumeroHabitacion.equals("") || tCapacidad.getText().equals("") || tDescripcion.getText().equals("") || tFoto.getText().equals("") || tValorPorDia.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos.");
        }
        else{
            String numero = tNumeroHabitacion.getText();
            String descripcion = tDescripcion.getText();
            String foto = tFoto.getText();
            double valorPorDia = Double.valueOf(tValorPorDia.getText());
            int capacidad = Integer.parseInt(tCapacidad.getText());
            this.hotel.crearHabitacion(numero, capacidad, descripcion, valorPorDia, foto);
            JOptionPane.showMessageDialog(null, "¡La habitación ha sido creada exitiosamente!");
            manejoDeArchivo();
            tNumeroHabitacion.setText("");
            tDescripcion.setText("");
            tCapacidad.setText("");
            tFoto.setText("");
            tValorPorDia.setText("");
        }
        
    }//GEN-LAST:event_bCrearActionPerformed

