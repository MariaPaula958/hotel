    public void manejoDeArchivo(){      
        
        try{
            this.archivo = new FileOutputStream("Hab_"+tNumeroHabitacion.getText()+".properties"); 
            propiedades.setProperty("NumeroDeHabitacion", tNumeroHabitacion.getText());
            propiedades.setProperty("Capacidad", tCapacidad.getText());
            propiedades.setProperty("CostoPorDia", "$"+tValorPorDia.getText());
            propiedades.setProperty("Descripcion", tDescripcion.getText());
            propiedades.setProperty("Disponibilidad", "Si");
            propiedades.setProperty("Fotografia", tFoto.getText()+".jpg");
            propiedades.store(archivo, "Información de la habitacion");
            
            this.habitacionNumero.add("Hab_"+tNumeroHabitacion.getText());
            FileWriter escribir = new FileWriter("C:\\Users\\Gestec\\Desktop\\Hotel\\HabitacionesCreadas.txt", true);
                    
            escribir.write(this.habitacionNumero.get(i));
            escribir.write("\n");
            escribir.close();
            i++;
                
            }
            catch (IOException exception){
                JOptionPane.showMessageDialog(null, exception);
            }
    }

